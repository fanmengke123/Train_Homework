print('{}\n{}\n{}\n{}\n{}\n{}'.format('A.请设置通话时长:', "1." + Bill_sets[0], "2." + Bill_sets[1], "3." + Bill_sets[2],
                                      "4." + Bill_sets[3], "5." + Bill_sets[4]))
# 提示用户输入选择通话时间
Aset = input("输入选择的通话时间编号：")
