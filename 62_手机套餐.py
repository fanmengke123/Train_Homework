def check(x, y):
    try:
        x = int(x)
    except:
        x = input('你输入的套餐不存在，请重新输入：')
        return check(x, y)
    if x > 0 and x <= y:
        return x
    else:
        x = input('你输入的套餐不存在，请重新输入：')
        return check(x, y)


# 可设置话费列表
Bill_sets = ['0分钟', '50分钟', '100分钟', '300分钟', '不限量']
# 可设置流量列表
flow_sets = ['0M', '500M', '1G', '5G', '不限量']
# 可设置短信列表
sms_sets = ['0条', '50条', '100条 ']
